public class Op{
    public static void main(String[]args){
        int a=10;      
        int b=20;
        //A program to perforam basic operations
        
        System.out.println("a+b = "+(a+b));
        System.out.println("a-b = "+(a-b));
        System.out.println("a*b = "+(a*b));
        System.out.println("a/b = "+(a/b));
        System.out.println("a%b = "+(a%b));
        System.out.println("a>b = "+(a>b));
        System.out.println("a<b = "+(a<b));
        System.out.println("a>=b = "+(a>=b));
        System.out.println("a<=b = "+(a<=b));
        System.out.println("a==b = "+(a==b));
        System.out.println("a++ = "+(a++));
        System.out.println("++a = "+(++a));
        System.out.println("b++ = "+(b++));
        System.out.println("++b = "+(++b));

    }
}