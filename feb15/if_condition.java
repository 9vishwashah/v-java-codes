import java.util.*;

public class if_condition {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int n = sc.nextInt(0);
        if (n > 0) {
            System.out.println("It is a positive no");
        } else {
            System.out.println("It is a negative no");
        }
        sc.close();

    }

}
